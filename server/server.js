const generateRandomName = () =>{
    let finalName = nameList[Math.floor(Math.random() * nameList.length)];
    return finalName
}
let activeUsers = [];

io.on("connection", (socket) => {
    console.log("client connected", socket.id);

    activeUsers.push({id:socket.id,name:generateRandomName()});
    io.emit("active-users", activeUsers);

    socket.on("send-message", (data) => {
        console.log("message with id", data, socket.id);
        const targetSocketId = data.selectedContact.id;
        socket.to(targetSocketId).emit("message-recieved", data.msg)

    })

    socket.on("disconnect", () => {
        console.log("client disconnected", socket.id);
        activeUsers = activeUsers.filter(data => data.id !== socket.id);
        io.emit("active-users", activeUsers);
    });
})



